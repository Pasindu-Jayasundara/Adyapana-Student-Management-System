    private void generate(String path) {
        try {

            HashMap<String, Object> parameter = new HashMap<>();
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/adyapana_db", "root", "Pasindu328@Bhathiya");

            JasperPrint report = JasperFillManager.fillReport(path, parameter, connection);
            JasperViewer.viewReport(report, false);

            connection.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

